document.addEventListener('DOMContentLoaded', () => {
  // Navigation Scroll Effect
  const header = document.getElementById('tm-header');
  window.addEventListener('scroll', () => {
    if (window.scrollY > 50) {
      header.classList.add('scrolled');
    } else {
      header.classList.remove('scrolled');
    }
  });

  // Language Switcher Logic
  const langSelect = document.getElementById('lang-select');

  // Prefer stored language, then browser language, fallback to zh
  let currentLang = localStorage.getItem('tm-lang');
  if (!currentLang) {
    const userLang = navigator.language || navigator.userLanguage;
    if (userLang.startsWith('en')) currentLang = 'en';
    else if (userLang.startsWith('ko')) currentLang = 'ko';
    else currentLang = 'zh';
  }

  if (langSelect) {
    langSelect.value = currentLang;
    langSelect.addEventListener('change', (e) => {
      localStorage.setItem('tm-lang', e.target.value);
      updateLanguage(e.target.value);
    });
  }

  // Apply language immediately
  updateLanguage(currentLang);

  // Intersection Observer for scroll reveal functionality (PXG-like fade animations)
  const reveals = document.querySelectorAll('.tm-reveal');
  const revealOptions = {
    threshold: 0.15,
    rootMargin: "0px 0px -50px 0px"
  };

  const revealOnScroll = new IntersectionObserver(function (
    entries,
    observer
  ) {
    entries.forEach(entry => {
      if (!entry.isIntersecting) {
        return;
      } else {
        entry.target.classList.add('active');
        observer.unobserve(entry.target);
      }
    });
  }, revealOptions);

  reveals.forEach(reveal => {
    revealOnScroll.observe(reveal);
  });
});

function updateLanguage(lang) {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (i18n[lang] && i18n[lang][key]) {
      el.textContent = i18n[lang][key];
    }
  });
}
